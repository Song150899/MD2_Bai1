package ThucHanh;

public class Th2_datatypes {
    public static void main(String[] args) {
        int i = 1;
        float f = 2.5f;
        double d = 23.5;
        boolean b = true;
        char c = 'C';
        String s = " Dượng đây";
        System.out.println("Kiểu dữ liệu int có kết quả là: " + i);
        System.out.println("Kiểu dữ liệu float có kết quả là: "+ f);
        System.out.println("Kiểu dữ liệu Double có kết quả là: "+ d);
        System.out.println("Kiểu dữ liệu Boolean có kết quả là: "+ b);
        System.out.println("Kiểu dữ liệu Char có kết quả là: "+ c);
        System.out.println("Kiểu dữ liệu String có kết quả là: "+ s);
    }
}
